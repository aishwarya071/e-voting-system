		<p>
			<ol class="i2">
				<big><li><b>General procedure for online voting</b></li></big>
				Online voting is initiated by a voting proposal followed by a discussion period. After the discussion period, the voting period is started. The voting is finished by calculating and publishing the voting results. Results of online votes are effective immediately following the publication of the results unless otherwise stated in the voting proposal.
				<big><li><b>Voting Proposal</b></li></big>
				Online voting is initiated by a proposal for voting which states the subject of voting. The proposal is posted on the information page which includes the exact and complete information about the type of voting, a candidate standing for that election, and the voting period. 
				<big><li><b>Discussion Period</b></li></big>
				Once the voting proposal is posted the discussion period is activated for 2 weeks. The discussion period should be used to discuss the voting and form opinions about the options which are available for voting.
				<big><li><b>Start of Voting</b></li></big>
				When the discussion period has finished the voting period is activated for all registered voters.
				<big><li><b>Voting Period</b></li></big>
				The voting period lasts for one week. During the voting period, registered voters can cast their votes by logging in with their Name and voting Id. Only votes cast within the voting period are considered for the results of the voting.
				<big><li><b>End of Voting</b></li></big>
				After one week the voting lines are terminated.The results of a vote are published on the website once the voting period is over. They include the name of the winning party and the total number of votes received by each candidate.
				<big><li><b>Anonymity</b></li></big>
				Online votes are anonymous. The information in which voters participated in the vote and for which option they voted aren't disclosed. Only the election officer is allowed to track this information for technically executing the vote. The information isn't used for other purposes and isn't made available to any other people.
				<big><li><b>Right to vote</b></li></big>
				All registered voters are eligible to vote if they fulfil the requirements for registering to vote. To vote one must register and obtain a valid voter Id. 
				<big><li><b>Requirements for registering to vote</b></li></big>
				You can enroll as a Voter if you:<br>
				- have attained the age of 18 years on the qualifying date i.e. 1st of January of the year of revision of electoral roll.<br>
				- are ordinarily resident of the part/polling area of the constituency where you want to be enrolled.<br>
				- are not disqualified to be enrolled as an elector.
				<big><li><b>How to register to vote</b></li></big>
				<u>To register as a voter:</u><br>
				Fill the registration form with your registered Voter-ID and personal details.<br>
				On submitting the form, you will be registered as a voter.<br>
				<u>To register as a candidate:</u><br>
				Fill the registration form with your registered Registration-ID and personal details.<br>
				On submitting the form, you will be registered as a Candidate.<br>
